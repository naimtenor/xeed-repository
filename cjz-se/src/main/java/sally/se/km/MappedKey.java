package sally.se.km;

public class MappedKey {

	private String whoKeys = "";
	
	private String whenKeys = "";
	
	private String whereKeys = "";
	
	private String whatKeys = "";
	
	private String whyKeys = "";
	
	private String howKeys = "";

	public String getWhoKeys() {
		return whoKeys;
	}

	public void setWhoKeys(String whoKeys) {
		this.whoKeys = whoKeys;
	}

	public String getWhenKeys() {
		return whenKeys;
	}

	public void setWhenKeys(String whenKeys) {
		this.whenKeys = whenKeys;
	}

	public String getWhereKeys() {
		return whereKeys;
	}

	public void setWhereKeys(String whereKeys) {
		this.whereKeys = whereKeys;
	}

	public String getWhatKeys() {
		return whatKeys;
	}

	public void setWhatKeys(String whatKeys) {
		this.whatKeys = whatKeys;
	}

	public String getWhyKeys() {
		return whyKeys;
	}

	public void setWhyKeys(String whyKeys) {
		this.whyKeys = whyKeys;
	}

	public String getHowKeys() {
		return howKeys;
	}

	public void setHowKeys(String howKeys) {
		this.howKeys = howKeys;
	}
	
}
